        msg = msg.Replace("    ", " ");
        msg = msg.Replace("   ", " ");
        msg = msg.Replace("  ", " ");
        msg = msg.Replace('\r', '\n');
        msg = msg.Replace(':', ';');
        List<string> list = new List<string>();
        string[] array = msg.Split(new char[]
        {
            '\n'
        });
        for (int i = 0; i < array.Length; i++)
        {
            int num = array[i].IndexOf("//");
