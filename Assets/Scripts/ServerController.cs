                {
                    string[] array3 = text.Replace(" ", "").Split(new char[]
                    {
                        '='
                    });
                    ClientConfig.ParseEquals(array3[0], array3[1]);
                }
                else
                {
                    ClientConfig.ParseOperator(text.Replace(" ", ""));
                }
            }
        }
        ClientConfig.EndConfig();
    }

